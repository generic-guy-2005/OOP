public abstract class Bank {
    private String name;
    
    public Bank(String name){
        this.name = name;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
    public abstract void eduSaving(boolean edu);
    public abstract void regSaving(boolean reg);
}
