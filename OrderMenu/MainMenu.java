    private void buttonReturnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonReturnActionPerformed
        double money = Double.parseDouble(textMoney.getText());
        double pay = Double.parseDouble(textTotalAll.getText());
        double ret;
        
        if(money < pay){
            JOptionPane.showMessageDialog(this, "Uangnya Kurang Mas/Mba", "Unsufficient Fund", JOptionPane.WARNING_MESSAGE);
        }
        else{
            ret = money - pay;
            textReturn.setText(Double.toString(ret));
        }
    }//GEN-LAST:event_buttonReturnActionPerformed

    private void completeButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_completeButtonActionPerformed
        if(textName.getText().isBlank()){
            JOptionPane.showMessageDialog(this, "Anda Siapa?", "Unknwon Customer", JOptionPane.WARNING_MESSAGE);
        }
        else if(textTotalAll.getText().isBlank()){
            JOptionPane.showMessageDialog(this, "Kosong? Anda Belanja Atau Tidak?", "Empty Transaction", JOptionPane.WARNING_MESSAGE);
        }
        else if(textId.getText().isBlank()){
            JOptionPane.showMessageDialog(this, "Alamatnya Mas/Mba :)", "Unknown ID", JOptionPane.WARNING_MESSAGE);
        }
        else if(textMoney.getText().isBlank() || textReturn.getText().isBlank()){
            JOptionPane.showMessageDialog(this, "Uangnya.....", "Payment Status", JOptionPane.WARNING_MESSAGE);
        }
        else{
            String details = "Nama: " + textName.getText() +
                    "\nID: " + textId.getText() +
                    "\nTotal: " + textTotalAll.getText() +
                    "\nReturn: " + textReturn.getText();
            JOptionPane.showMessageDialog(this, "Transaksi Selelasi, Jangan Kembali\n" + details, "Complete", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_completeButtonActionPerformed

