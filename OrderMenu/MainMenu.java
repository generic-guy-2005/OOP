    private void totalButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_totalButtonActionPerformed
        double foodCost, drinkCost;
        
        String food = textFoodTotal.getText();
        if(food.isBlank()){
            foodCost = 0;
        }
        else{
            foodCost = Double.parseDouble(textFoodTotal.getText());
        }
        
        String drink = textDrinkTotal.getText();
        if(drink.isBlank()){
            drinkCost = 0;
        }
        else{
            drinkCost = Double.parseDouble(textDrinkTotal.getText());
        }
        
        double price = foodCost + drinkCost;
        textPrice.setText(Double.toString(price));
        
        double tax;
        tax = 0;
        double finalPrice;
        
        if(price > 50000){
            tax = price * 0.1;
            textTax.setText(Double.toString(tax));
        }
        else {
            textTax.setText("0");
        }
        
        finalPrice = price + tax;
        textTotalAll.setText(Double.toString(finalPrice));
    }//GEN-LAST:event_totalButtonActionPerformed

